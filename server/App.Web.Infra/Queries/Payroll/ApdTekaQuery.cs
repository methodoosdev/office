					 PERIODOI_DATA.EMP_CATEGORY,
					 PERIODOI_DATA.OAED_EPID,
					 PERIODOI_DATA.OAED_EMP_TYPE,
					 CMP_YPOKAT.COD_YPOKAT,
					 CMP_YPOKAT.DESCR,
					 PERIODOI_DATA.REMS,
					 EMPLOYEE.CODE,
					 PERIODOI_DATA.EMP_SPECIAL_TYPE,
					 MK.POSO_AGAINST,
					 MK.KRAT_ERZ_SYN,
					 MK.POSO_ERGAZ_SYNERGASIA_1,
					 MK.POSO_ERGAZ_SYNERGASIA_2,
					 ISNULL(TAMEIO.DESCR, '9999');
		";

