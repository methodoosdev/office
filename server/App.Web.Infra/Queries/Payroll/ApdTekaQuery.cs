									  AND (
										  PERIODOI_DATA.EMP_TYPE IN (2, 9)
										  OR (
											  PERIODOI_DATA.EMP_TYPE = 4
											  AND PERIODOI_DATA.EMP_SPECIAL_TYPE IN (12, 20)
										  )
									  )
									 THEN dbo.ufn_XARTOSHMO_VALUE(
											  PERIODOI_DATA.ID_EMP,
											  PERIODOI_DATA.XRISI,
											  PERIODOI_DATA.ID_PERIODOS,
											  0
										  )
								 ELSE 0
							 END
						 )
					   + SUM(ISNULL(MK.KRAT_ERZ_SYN, 0)) AS SINOLIKOKOSTOS,
				   SUM(
					   CASE
						   WHEN (PERIODOI_DATA.EMP_TYPE = 9)
								OR (PERIODOI_DATA.EMP_TYPE = 2
									AND PERIODOI_DATA.ARTIST_KIND = 0
									AND PERIODOI_DATA.ARTIST_TYPE = 0)
							   THEN ISNULL(PERIODOI_DATA.VAT_VALUE, 0)
						   ELSE 0
					   END
				   ) AS VAT_VALUE,
				   dbo.ufn_grkl(PERIODOI_DATA.REMS) AS REMS,
				   ISNULL(TAMEIO.DESCR, '9999') AS TAMEIO_DESCR,
				   EMPLOYEE.CODE,
				   SUM(
					   CASE
						   WHEN PERIODOI_DATA.EMP_TYPE = 2
								OR (PERIODOI_DATA.EMP_TYPE = 9 AND PERIODOI_DATA.artist_type = 10)
								OR (PERIODOI_DATA.EMP_TYPE = 4
									AND PERIODOI_DATA.EMP_SPECIAL_TYPE IN (12, 20, 21, 23))
							   THEN ISNULL(
										ROUND(
											dbo.UFN_XARTOSHMO_VALUE(
												PERIODOI_DATA.ID_EMP,
												PERIODOI_DATA.XRISI,
												PERIODOI_DATA.ID_PERIODOS,
												0
											),
											2
										),
										0
									)
						   ELSE 0
					   END
				   ) AS XARTOSHMO,
				   SUM(ISNULL(POSO_ERGOD_3, 0)) AS POSO_ERGOD_3,
				   SUM(ISNULL(POSO_ERGAZ_3, 0)) AS POSO_ERGAZ_3,
				   SUM(ISNULL(KRAT_ika.POSO_ERGOD, 0)) AS POSO_ERGOD_ika,
				   SUM(ISNULL(KRAT_ika.POSO_ERGAZ, 0)) AS POSO_ERGAZ_ika,
				   SUM(ISNULL(KRAT_EPIK.POSO_ERGOD, 0)) AS POSO_ERGOD_EPIK,
				   SUM(ISNULL(KRAT_EPIK.POSO_ERGAZ, 0)) AS POSO_ERGAZ_EPIK,
				   CAST(NULL AS VARCHAR) AS TPTE,
				   CAST(NULL AS VARCHAR) AS TPTE_TEKA,
				   CAST(NULL AS VARCHAR) AS AMOE,
				   SUM(
					   ISNULL(MK.POSO_ERGAZ_SYNERGASIA_1, 0)
					   + ISNULL(MK.POSO_ERGAZ_SYNERGASIA_2, 0)
				   ) AS KRAT_ERGAZ_SYNERGASIA,
				   SUM(
					   CASE
						   WHEN PERIODOI_DATA.EMP_TYPE IN (5, 10)
								AND ISNULL(PERIODOI_DATA.SSE_WEEK_HOURS_TCHR, 0) > 0
							   THEN dbo.eRound(
										(PERIODOI_DATA.WEEK_HOURS_TCHR / PERIODOI_DATA.SSE_WEEK_HOURS_TCHR) * 166.67,
										2
									)
						   ELSE 0
					   END
				   ) AS NNT_VAL,
				   ISNULL(PERIODOI_DATA.TEI_EPIDOTHSH, 0)
					   + ISNULL(PERIODOI_DATA.PROKATAVOLI, 0)
					   + ISNULL(
							 (
								 SELECT SUM(PERIODOS_PAROXES.POSO)
								 FROM PERIODOS_PAROXES
								 WHERE PERIODOS_PAROXES.ID_EMP =
										   PERIODOI_DATA.ID_EMP
								   AND PERIODOS_PAROXES.XRISI =
										   PERIODOI_DATA.XRISI
								   AND PERIODOS_PAROXES.ID_PERIODOS =
										   PERIODOI_DATA.ID_PERIODOS
							 ),
							 0
						 )
					   + SUM(
							 ISNULL(PERIODOI_DATA.STRIKE_SUM, 0)
							 + ISNULL(PERIODOI_DATA.STASI_SUM, 0)
						 ) AS PROKATAVOLI,
				   CAST(NULL AS FLOAT) AS PAROXI,
				   CAST(NULL AS FLOAT) AS TEI_EPIDOTHSH,
				   ISNULL(
					   (
						   SELECT SUM(PERIODOS_PAROXES.POSO_ERGOD)
						   FROM PERIODOS_PAROXES
						   WHERE PERIODOS_PAROXES.ID_EMP = PERIODOI_DATA.ID_EMP
							 AND PERIODOS_PAROXES.XRISI = PERIODOI_DATA.XRISI
							 AND PERIODOS_PAROXES.ID_PERIODOS = PERIODOI_DATA.ID_PERIODOS
					   ),
					   0
				   ) AS PAROXI_ERD,
				   SUM(MK.EPIDOTHSH_OAED) AS EPIDOTHSH_OAED,
				   SUM(
					   dbo.ufn_PLIROTEO(
						   PERIODOI_DATA.id_emp,
						   PERIODOI_DATA.xrisi,
						   PERIODOI_DATA.id_periodos
					   ) + 0
				   ) AS KOSTOSERGOD,
				   CAST(NULL AS FLOAT) AS PASENT_POSO,
				   CAST(NULL AS VARCHAR) AS OD_PROG,
				   CAST(NULL AS VARCHAR) AS DESCR_EP,
				   CAST('-999' AS VARCHAR) AS MIS,
				   0 AS ID,
				   SUM(ISNULL(PANK.M_APODOXES_NOKRAT, 0)) AS M_APODOXES_NOKRAT
			FROM PERIODOI_DATA
				 OUTER APPLY (
					 SELECT *
					 FROM dbo.ufn_GravDays(
							  PERIODOI_DATA.ID_EMP,
							  PERIODOI_DATA.XRISI,
							  PERIODOI_DATA.ID_PERIODOS
						  )
					 WHERE PERIODOI_DATA.CALCTYPE1 = 2
				 ) V2
				 JOIN (
					 SELECT PD.ID_EMP,
							PD.OAED_MIS
					 FROM PERIODOI_DATA PD
					 WHERE PD.ID_PERIODOS = 0
					   AND PD.XRISI = 0
				 ) PDP
					 ON PDP.ID_EMP = PERIODOI_DATA.ID_EMP
				 LEFT JOIN GRADATIONS GR
					 ON GR.GID = PERIODOI_DATA.GRADATION1
				 LEFT JOIN ADMINISTRATION AD
					 ON AD.ID_ADMIN = PERIODOI_DATA.ID_ADMINISTRATION
				 LEFT JOIN ADMINISTRATION_SUB ADS
					 ON ADS.ID_ADMINS = PERIODOI_DATA.ID_ADMINISTRATION_SUB
				 LEFT JOIN V_MISTHODOTIKH_KRATHSEIS MK
					 ON PERIODOI_DATA.ID_PERIODOS = MK.ID_PERIODOS
					AND PERIODOI_DATA.ID_EMP = MK.ID_EMP
					AND PERIODOI_DATA.XRISI = MK.XRISI
				 LEFT JOIN (
					 SELECT PA.ID_PERIODOS,
							PA.XRISI,
							PA.ID_EMP,
							MAX(ISNULL(T.DESCR, '')) AS DESCR
					 FROM PERIODOS_APOD PA
						  JOIN PER_KRATISEIS PK
							  ON PK.ID_PERIODOS_APOD = PA.ID_PERIODOS_APOD
						  JOIN TAMEIA T
							  ON T.ID_TAMEIO = PK.ID_TAMEIO
							 AND T.IS_BAREA = 1
						  JOIN OM_TAMEIA OT
							  ON OT.ID_TAMEIO = T.ID_TAMEIO
							 AND OT.IS_MAIN = 1
					 GROUP BY PA.ID_PERIODOS,
							  PA.XRISI,
							  PA.ID_EMP
				 ) AS TAMEIO
					 ON TAMEIO.ID_PERIODOS = PERIODOI_DATA.ID_PERIODOS
					AND TAMEIO.XRISI = PERIODOI_DATA.XRISI
					AND TAMEIO.ID_EMP = PERIODOI_DATA.ID_EMP
				 LEFT JOIN V_PERIODOS_PAROXES PP
					 ON PERIODOI_DATA.ID_EMP = PP.ID_EMP
					AND PERIODOI_DATA.XRISI = PP.XRISI
					AND PERIODOI_DATA.ID_PERIODOS = PP.ID_PERIODOS
				 JOIN CMP_YPOKAT
					 ON CMP_YPOKAT.COD_YPOKAT = PERIODOI_DATA.COD_YPOKAT
					AND CMP_YPOKAT.ID_CMP = PERIODOI_DATA.ID_CMP
				 LEFT JOIN (
					 SELECT SUM(
								PA.M_APODOXES
								- ISNULL(PA.FMY_SINEK, 0)
								- ISNULL(PA.FMY_EKT_EISF_APOD, 0)
							) AS ISOP_KATHARA,
							/*SUM(IsNull(PK.POSO_ERGAZ,0))*/ 0.00 AS ISOP_POSO_ERGAZ,
							PA.ID_EMP,
							PA.XRISI,
							PA.ID_PERIODOS
					 FROM PERIODOS_APOD PA
						  LEFT JOIN (
							  SELECT ID_PERIODOS_APOD,
									 SUM(ISNULL(POSO_ERGAZ, 0)) AS POSO_ERGAZ
							  FROM PER_KRATISEIS PK
							  GROUP BY ID_PERIODOS_APOD
						  ) PK
							  ON PK.ID_PERIODOS_APOD = PA.ID_PERIODOS_APOD
					 WHERE PA.ID_APODOXHS = 11000
					 GROUP BY PA.ID_EMP,
							  PA.XRISI,
							  PA.ID_PERIODOS
				 ) AS ISOP
					 ON ISOP.ID_PERIODOS = PERIODOI_DATA.ID_PERIODOS
					AND ISOP.ID_EMP = PERIODOI_DATA.ID_EMP
					AND ISOP.XRISI = PERIODOI_DATA.XRISI
				 LEFT JOIN (
					 SELECT dbo.PERIODOI_DATA.ID_EMP,
							dbo.PERIODOI_DATA.XRISI,
							dbo.PERIODOI_DATA.ID_PERIODOS,
							SUM(dbo.PER_KRATISEIS.POSO_ERGAZ) AS POSO_ERGAZ,
							SUM(dbo.PER_KRATISEIS.POSO_ERGOD) AS POSO_ERGOD
					 FROM dbo.PER_KRATISEIS
						  JOIN dbo.PERIODOS_APOD
							  ON dbo.PERIODOS_APOD.ID_PERIODOS_APOD = dbo.PER_KRATISEIS.ID_PERIODOS_APOD
						  JOIN dbo.PERIODOI_DATA
							  ON dbo.PERIODOI_DATA.ID_EMP = dbo.PERIODOS_APOD.ID_EMP
							 AND dbo.PERIODOI_DATA.XRISI = dbo.PERIODOS_APOD.XRISI
							 AND dbo.PERIODOI_DATA.ID_PERIODOS = dbo.PERIODOS_APOD.ID_PERIODOS
						  JOIN dbo.TAMEIA
							  ON dbo.TAMEIA.ID_TAMEIO = dbo.PER_KRATISEIS.ID_TAMEIO
						  LEFT OUTER JOIN dbo.OM_TAMEIA
							  ON dbo.OM_TAMEIA.ID_OMADA = dbo.PERIODOI_DATA.ID_OMADA
							 AND dbo.OM_TAMEIA.ID_TAMEIO = dbo.PER_KRATISEIS.ID_TAMEIO
					 WHERE (dbo.TAMEIA.IS_IKA = 1)
					 GROUP BY dbo.PERIODOI_DATA.ID_EMP,
							  dbo.PERIODOI_DATA.XRISI,
							  dbo.PERIODOI_DATA.ID_PERIODOS
				 ) AS KRAT_IKA
					 ON KRAT_IKA.ID_EMP = PERIODOI_DATA.ID_EMP
					AND KRAT_IKA.XRISI = PERIODOI_DATA.XRISI
					AND KRAT_IKA.ID_PERIODOS = PERIODOI_DATA.ID_PERIODOS
				 LEFT JOIN (
					 SELECT dbo.PERIODOI_DATA.ID_EMP,
							dbo.PERIODOI_DATA.XRISI,
							dbo.PERIODOI_DATA.ID_PERIODOS,
							SUM(dbo.PER_KRATISEIS.POSO_ERGAZ) AS POSO_ERGAZ,
							SUM(dbo.PER_KRATISEIS.POSO_ERGOD) AS POSO_ERGOD
					 FROM dbo.PER_KRATISEIS
						  JOIN dbo.PERIODOS_APOD
							  ON dbo.PERIODOS_APOD.ID_PERIODOS_APOD = dbo.PER_KRATISEIS.ID_PERIODOS_APOD
						  JOIN dbo.PERIODOI_DATA
							  ON dbo.PERIODOI_DATA.ID_EMP = dbo.PERIODOS_APOD.ID_EMP
							 AND dbo.PERIODOI_DATA.XRISI = dbo.PERIODOS_APOD.XRISI
							 AND dbo.PERIODOI_DATA.ID_PERIODOS = dbo.PERIODOS_APOD.ID_PERIODOS
						  JOIN dbo.TAMEIA
							  ON dbo.TAMEIA.ID_TAMEIO = dbo.PER_KRATISEIS.ID_TAMEIO
						  LEFT OUTER JOIN dbo.OM_TAMEIA
							  ON dbo.OM_TAMEIA.ID_OMADA = dbo.PERIODOI_DATA.ID_OMADA
							 AND dbo.OM_TAMEIA.ID_TAMEIO = dbo.PER_KRATISEIS.ID_TAMEIO
					 WHERE (dbo.TAMEIA.IS_IKA = 0)
					   AND dbo.PER_KRATISEIS.ID_TAMEIO NOT IN (
							   59, 60, 61, 62, 156, 179, 180, 237, 239, 240, 286, 327, 346,
							   365, 366, 394, 430, 452, 453, 454, 455, 456, 457, 458, 459,
							   460, 484, 485, 463, 464, 465, 466, 467, 471, 472, 473, 475,
							   476, 5036, 5037, 5038, 5039, 5046, 5050, 5051, 5052, 5054,
							   5055, 5137, 5292, 500, 501, 84, 85, 88, 89, 142, 143, 192,
							   284, 328, 329, 330, 354, 403, 404, 405, 406, 407, 408, 409,
							   410, 411, 413, 414, 415, 417, 418, 419, 420, 421, 422, 423,
							   424, 431, 516, 5040, 5041, 5042, 5043, 5044, 5045, 5049,
							   5053, 5056, 5057, 5058, 5119, 534, 535
						   )
					 GROUP BY dbo.PERIODOI_DATA.ID_EMP,
							  dbo.PERIODOI_DATA.XRISI,
							  dbo.PERIODOI_DATA.ID_PERIODOS
				 ) AS KRAT_EPIK
