							PA.ID_PERIODOS,
							SUM(ISNULL(PA.M_APODOXES_NOKRAT, 0)) AS M_APODOXES_NOKRAT
					 FROM PERIODOS_APOD PA
					 GROUP BY PA.ID_EMP,
							  PA.XRISI,
							  PA.ID_PERIODOS
				 ) PANK
					 ON PERIODOI_DATA.ID_EMP = PANK.ID_EMP
					AND PERIODOI_DATA.XRISI = PANK.XRISI
					AND PERIODOI_DATA.ID_PERIODOS = PANK.ID_PERIODOS,
				 PERIODOI,
				 EMPLOYEE,
				 CMP
			WHERE PERIODOI_DATA.ID_EMP = EMPLOYEE.ID_EMP
			  AND PERIODOI_DATA.ID_CMP = CMP.ID_CMP
