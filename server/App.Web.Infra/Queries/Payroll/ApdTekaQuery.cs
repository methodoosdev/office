        public static string Employees => @"
			DECLARE @StartOfMonth date = DATEFROMPARTS(@pYear, @pPeriod, 1);
			DECLARE @EndOfMonth   date = EOMONTH(@StartOfMonth);

			SELECT 
				  V.ID_EMP AS EmployeeId
				, @pYear   AS [Year]
				, @pPeriod AS [Period]
				, V.SURNAME + ' ' + V.NAME AS EmployeeName
			FROM 
				dbo.vpr_WRKListFull(@pYear,'admin',@pCompanyId,'*',0,0) AS V
			WHERE 
				V.HRDATE <= @EndOfMonth   
				AND (V.FRDATE IS NULL OR V.FRDATE >= @StartOfMonth);
        ";
