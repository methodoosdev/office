					 PERIODOI_DATA.S_KOSTOS,
					 PERIODOI_DATA.PROKATAVOLI,
					 PERIODOI_DATA.TEI_EPIDOTHSH,
					 PERIODOI_DATA.MERIKH_APASX,
					 PERIODOI_DATA.SALARY_AGREED,
					 PERIODOI_DATA.MERIKI_SALARY_AGREED,
